package com.example.example_mod.config;

public class ModConfig {

    private boolean enableMod;

    public ModConfig() {
        this.enableMod = true;
    }

    public boolean isEnableMod() {
        return enableMod;
    }

    public void setEnableMod(boolean enableMod) {
        this.enableMod = enableMod;
    }

}